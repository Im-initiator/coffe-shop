    void loadData() {
        modelTB.setRowCount(0);
        String sql = "select CHITIETHD.MANUOCUONG,TENNUOCUONG,CHITIETHD.DONGIA ,SUM(soluong) 'TSL',SUM(THANHTIEN) 'TTT' from CHITIETHD,NuocUong where CHITIETHD.MANUOCUONG=NUOCUONG.MANUOCUONG group by CHITIETHD.MANUOCUONG,TENNUOCUONG,CHITIETHD.DONGIA";
        try {
            ConnectDB.getConnection();
            ResultSet rs = ConnectDB.executeQuery(sql);
            while (rs.next()) {
                Vector r = new Vector();
                r.add(rs.getString("MANUOCUONG").trim());
                r.add(rs.getString("TENNUOCUONG").trim());
                r.add(rs.getFloat("DONGIA"));
                r.add(rs.getInt("TSL"));
                r.add(rs.getFloat("TTT"));
                modelTB.addRow(r);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Lỗi", "Thông báo", 1);
        }
        ConnectDB.close();
    }
