    private void btnTKDTTheoNgayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKDTTheoNgayActionPerformed
        // TODO add your handling code here:
        if (!hoTro.ktraNgay(txtNgayLap)) {
            return;
        }
        String sql = "set dateformat dmy; select sum(thanhToan) from HOADON where NGAYRAHD='" + txtNgayLap.getText().trim() + "'";
        try {
            ConnectDB.getConnection();
            ResultSet rs = ConnectDB.executeQuery(sql);
            if (rs.next()) {
                txtDoanhThu.setText("" + rs.getFloat(1));
            }
            txtNgayLap.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Lỗi doanh thu", "Thông báo", 1);
        }
        ConnectDB.close();
    }//GEN-LAST:event_btnTKDTTheoNgayActionPerformed

    private void btnNVLapNhieuHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNVLapNhieuHDActionPerformed
        // TODO add your handling code here:
        String sql = "select TENNV,SL from nhanvien nv,(select Manv,count(*) 'SL' from HOADON group by MANV having count(*)>=all(select count(*) from HOADON group by MANV)) tk where tk.MANV=nv.MANV";
        try {
            ConnectDB.getConnection();
            ResultSet rs = ConnectDB.executeQuery(sql);
            String s = "";
            while (rs.next()) {
                s += "Tên nhân viên:" + rs.getString("tennv") + "-Số hoá đơn đã lập:" + rs.getInt("SL") + "\n";
            }
            s.substring(0, s.length() - 1);
            txaThongTin.setText(s);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Lỗi", "Thông báo", 1);
        }
        ConnectDB.close();
    }//GEN-LAST:event_btnNVLapNhieuHDActionPerformed

