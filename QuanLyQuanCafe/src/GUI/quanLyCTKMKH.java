    void viewKH(String maLoai, String maKH) {
        txtMa.setEditable(false);
        khachHang kh = khachHangDAO.layTTKH(maLoai, maKH);
        lbMa.setText(kh.getMakh());
        txtMa.setText(kh.getMakh());
        txtTenKH.setText(kh.getTenKh());
        if (kh.getGioiTinh().toLowerCase().equals("nam")) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        txtDiaChi.setText(kh.getDiaChi());
        txtSDT.setText(kh.getSdt());
        lbTenKH.setText(kh.getTenKh());
        lbTenLoai.setText(loaiKHDAO.timLoaiKH(kh.getMaLoaiKH()).getTenLoaiKH());
        lbGiamGia.setText(loaiKHDAO.timLoaiKH(kh.getMaLoaiKH()).getGiamGia() + "");
        cbbTenLoaiKH.setSelectedItem(loaiKHDAO.timLoaiKH(kh.getMaLoaiKH()).getTenLoaiKH().trim());
        btnThemKH.setEnabled(false);
    }
