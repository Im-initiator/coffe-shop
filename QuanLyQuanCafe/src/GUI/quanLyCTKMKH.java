    void loadDataKH() {
        modelTBKH.setRowCount(0);
        dsKH = khachHangDAO.getdsKH();
        for (khachHang kh : dsKH) {
            Vector r = new Vector();
            r.add(kh.getMakh());
            r.add(kh.getTenKh());
            r.add(kh.getGioiTinh());
            r.add(kh.getDiaChi());
            r.add(kh.getSdt());
            r.add(loaiKHDAO.timLoaiKH(kh.getMaLoaiKH()).getTenLoaiKH());
            modelTBKH.addRow(r);
        }
    }

    void xoaTrangLKH() {
        txtMaloai.setEditable(true);
        txtMaloai.setText("");
        txtMaloai.requestFocus();
        txtGiamGia.setText("");
        txtTenLoai.setText("");
        btnThem.setEnabled(true);
    }

    void loadDataLoaiKH() {
        cbbTenLoaiKH.removeAllItems();
        while (root.getChildCount() != 0) {
            root.remove(root.getChildCount() - 1);
        }
        dsLoaiKH = loaiKHDAO.getdsLoaiKH();
        for (loaiKH lkh : dsLoaiKH) {
            cbbTenLoaiKH.addItem(lkh.getTenLoaiKH());
            DefaultMutableTreeNode parent = new DefaultMutableTreeNode(lkh);
            root.add(parent);
        }
        modelTree.reload(root);
        for (int i = 0; i < treeLoaiKH.getRowCount(); i++) {
            treeLoaiKH.expandRow(i);
        }
    }

