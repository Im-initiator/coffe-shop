    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (hoTro.ktraRong(txtMaloai) || hoTro.ktraRong(txtTenLoai) || hoTro.ktraRong(txtGiamGia)) {
            return;
        }
        String maLoai = txtMaloai.getText().trim();
        String tenLoai = txtTenLoai.getText().trim();
        if (JOptionPane.showConfirmDialog(null, "Bạn muốn thêm loại " + tenLoai + " ?", "Thông báo", JOptionPane.YES_NO_OPTION, 1) == JOptionPane.YES_OPTION) {
            float giamGia = Float.parseFloat(txtGiamGia.getText().trim());
            int i = loaiKHDAO.themLoaiKH(maLoai, tenLoai, giamGia);
            if (i != -1) {
                JOptionPane.showMessageDialog(null, "Thêm thành công", "Thông báo", 1);
                xoaTrangLKH();
                loadDataLoaiKH();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:      
        if (treeLoaiKH.getSelectionPath() != null) {
            DefaultMutableTreeNode selectNode = (DefaultMutableTreeNode) treeLoaiKH.getLastSelectedPathComponent();
            if (selectNode.getUserObject() instanceof loaiKH) {
                if (JOptionPane.showConfirmDialog(null, "Bạn muốn xoá loại " + txtTenLoai.getText().trim() + " ?", "Thông báo", JOptionPane.YES_NO_OPTION, 1) == JOptionPane.YES_OPTION) {
                    int i = loaiKHDAO.xoaLoaiKH(txtMaloai.getText());
                    if (i != -1) {
                        JOptionPane.showMessageDialog(null, "Đã xoá", "Thông báo", 1);
                        loadDataLoaiKH();
                        loadDataKH();
                        xoaTrangLKH();
                        xoaTrangKH();
                    }
                }
            } else {
                if (JOptionPane.showConfirmDialog(null, "Bạn muốn xoá tất cả loại khách hàng?", "Thông báo", JOptionPane.YES_NO_OPTION, 1) == JOptionPane.YES_OPTION) {
                    loaiKHDAO.xoaLoaiKH();
                    loadDataKH();
                    loadDataLoaiKH();
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Mời bạn chọn loại khách hàng để xoá", "Thông báo", 1);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

