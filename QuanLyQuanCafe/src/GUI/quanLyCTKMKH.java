    private void treeLoaiKHMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_treeLoaiKHMousePressed
        // TODO add your handling code here:

        if (root.getChildCount() == 0 || treeLoaiKH.getSelectionPath() == null) {
            return;
        }
        DefaultMutableTreeNode selecNode = (DefaultMutableTreeNode) treeLoaiKH.getLastSelectedPathComponent();
        if (selecNode == root) {
            xoaTrangKH();
            xoaTrangLKH();
            loadDataKH();
            return;
        }
        Object selectObject = selecNode.getUserObject();
        if (selectObject instanceof loaiKH) {
            txtMaloai.setText(((loaiKH) selectObject).getMaLoaiKH());
            txtTenLoai.setText(((loaiKH) selectObject).getTenLoaiKH());
            txtGiamGia.setText(((loaiKH) selectObject).getGiamGia() + "");
            btnThem.setEnabled(false);
            txtMaloai.setEditable(false);
            modelTBKH.setRowCount(0);
            for (khachHang kh : khachHangDAO.layTTKH(((loaiKH) selectObject).getMaLoaiKH())) {
                Vector r = new Vector();
                r.add(kh.getMakh());
                r.add(kh.getTenKh());
                r.add(kh.getGioiTinh());
                r.add(kh.getDiaChi());
                r.add(kh.getSdt());
                r.add(kh.getMaLoaiKH());
                modelTBKH.addRow(r);
                xoaTrangKH();
            }
        }

    }//GEN-LAST:event_treeLoaiKHMousePressed

