    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        // TODO add your handling code here:
        if (hoTro.ktraRong(txtMatKhau) || hoTro.ktraRong(txtMatKhauMoi) || hoTro.ktraRong(txtNhapLaiMK)) {
            return;
        }
        if (nhanVienDAO.timMKTheoMa(maNV).equals(hoTro.md5(txtMatKhau.getText().trim()))) {
            if (txtMatKhauMoi.getText().equals(txtNhapLaiMK.getText())) {
                int i = nhanVienDAO.doiMatKhau(maNV, hoTro.md5(txtMatKhauMoi.getText().trim()));
                if (i != -1) {
                    JOptionPane.showMessageDialog(null, "Đổi mật khẩu thành công", "Thông báo", 1);
                    dlDoiMatKhau.dispose();
                    setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Nhập lại mật khẩu không giống nhau", "Thông báo", 1);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Mật khẩu không chính xác", "Thông báo", 1);
        }

    }//GEN-LAST:event_btnXacNhanActionPerformed

