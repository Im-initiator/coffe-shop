    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row >= 0) {
            if (JOptionPane.showConfirmDialog(null, "Bạn muốn xoá nước:" + modelTb.getValueAt(row, 1).toString() + "?", "Thông báo", JOptionPane.YES_NO_OPTION, 1) == JOptionPane.YES_OPTION) {
                modelTb.removeRow(row);
                tinhTien();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:       
        String ktr = "NK\\d{2,}";
        System.out.println(txtMaNhap.getText());
        if (!txtMaNhap.getText().toUpperCase().matches(ktr)) {
            JOptionPane.showMessageDialog(null, "Mã nhập có dạng NKxx", "Thông báo", 1);
            txtMaNhap.requestFocus();
            return;
        }

        String maNhap = txtMaNhap.getText().trim();
        if (JOptionPane.showConfirmDialog(null, "Bạn muốn nhập phiếu này với mã nhập:" + maNhap, "Thông báo", JOptionPane.YES_NO_OPTION, 1) == JOptionPane.YES_OPTION) {
            SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy hh:mm:ss");
            String noiDungFile = "Mã nhập:" + maNhap + "\t\tNgày lập:" + df.format(Calendar.getInstance().getTime()) + "\nTên nhân viên:" + txtTenNV.getText() + "\n=========================================================================================================" + "\nTên nước\t\tSố lượng\t\tĐơn giá\t\tThành tiền\n";
            int j = nhapKhoDAO.insert(maNhap, maNV, Float.parseFloat(txtTongCong.getText().trim()));
            if (j != -1) {
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    chiTietNKDAO.insert(maNhap, modelTb.getValueAt(i, 0).toString(), Integer.parseInt(modelTb.getValueAt(i, 3).toString()), Float.parseFloat(modelTb.getValueAt(i, 2).toString()), Float.parseFloat(modelTb.getValueAt(i, 4).toString()));
                    noiDungFile += modelTb.getValueAt(i, 1).toString() + "\t\t" + modelTb.getValueAt(i, 3).toString() + "\t\t\t" + modelTb.getValueAt(i, 2).toString() + "\t\t" + modelTb.getValueAt(i, 4).toString() + "\n";
                }
                noiDungFile += "=========================================================================================================\n\t\t\t\t\t\t\tThành tiền:" + txtTongCong.getText();
                createNew();
                j = hoTro.writeFile(maNhap, noiDungFile);
                if (j != -1) {
                    JOptionPane.showMessageDialog(null, "Nhập kho thành công", "Thông báo", 1);
                    hoTro.openFile(maNhap);
                }
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

