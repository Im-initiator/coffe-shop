    void nhapNuoc() {

        String tenNuoc = cbbTenNuoc.getSelectedItem().toString();
        int soLuong = Integer.parseInt(spnSoLuong.getValue().toString());
        for (int i = 0; i < modelTb.getRowCount(); i++) {
            if (modelTb.getValueAt(i, 1).toString().equals(tenNuoc)) {
                int soluongbd = Integer.parseInt(modelTb.getValueAt(i, 3).toString());
                soluongbd += soLuong;
                modelTb.setValueAt(soluongbd, i, 3);
                modelTb.setValueAt(soluongbd * Float.parseFloat(modelTb.getValueAt(i, 2).toString()), i, 4);
                return;
            }
        }
        float giaNhap = Float.parseFloat(txtgiaNhap.getText().trim());
        float thanhTien = soLuong * giaNhap;
        Vector r = new Vector();
        r.add(nuocUongDAO.timMaNuoc(tenNuoc));
        r.add(tenNuoc);
        r.add(giaNhap);
        r.add(soLuong);
        r.add(thanhTien);
        modelTb.addRow(r);
    }

    void tinhTien() {
        float sum = 0;
        for (int i = 0; i < modelTb.getRowCount(); i++) {
            sum += Float.parseFloat(modelTb.getValueAt(i, 4).toString());
        }
        txtTongCong.setText("" + sum);
    }
