    private void btnXoa1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoa1ActionPerformed
        // TODO add your handling code here:
        rowBan = tbBan.getSelectedRow();
        int index = cbbBan.getSelectedIndex();
        if (rowBan < 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn bàn", "Thông báo", 1);
            return;
        }
        int tinhTrang = banDAO.layTinhTrangTheoMa(tbBan.getValueAt(rowBan, 0).toString());
        if (tinhTrang == 0) {
            JOptionPane.showMessageDialog(null, tbBan.getValueAt(rowBan, 1) + " Chưa có người ở bàn này!!!!!", "Thông báo", 1);
            return;
        }
        if (rowBan == index) {
            JOptionPane.showMessageDialog(null, "Bạn đang ở " + tbBan.getValueAt(rowBan, 1).toString().toLowerCase() + " rồi nên không thể chuyển!!!!!", "Thông báo", 1);
            return;
        }
        tinhTrang = banDAO.layTinhTrangTheoMa(tbBan.getValueAt(index, 0).toString());
        if (tinhTrang == 1) {
            JOptionPane.showMessageDialog(null, tbBan.getValueAt(index, 1) + " này đã có người!!!", "Thông báo", 1);
            return;
        }
        if (JOptionPane.showConfirmDialog(null, "Bạn muốn chuyển từ " + modelTbBan.getValueAt(rowBan, 1).toString() + " sang " + modelTbBan.getValueAt(index, 1).toString(), "Thông báo", JOptionPane.YES_NO_OPTION, 1) == JOptionPane.YES_OPTION) {
            dsMonTungBan.set(index, ((Vector) dsMonTungBan.get(rowBan)));
            modelTbNuocUong.setRowCount(0);
            dsMonTungBan.set(rowBan, new Vector());

            banDAO.updateTinhTrang(tbBan.getValueAt(index, 0).toString(), true);
            banDAO.updateTinhTrang(tbBan.getValueAt(rowBan, 0).toString(), false);
            JOptionPane.showMessageDialog(null, "Chuyển bàn thành công", "Thông báo", 1);
            tbBan.setValueAt("Trống", rowBan, 2);
            tbBan.setValueAt("Có người", index, 2);
            tbBanMousePressed(null);
        }
    }//GEN-LAST:event_btnXoa1ActionPerformed

