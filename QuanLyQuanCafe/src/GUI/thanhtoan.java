    void loadTT() {
        banDAO.updateTinhTrang();
        dsMonTungBan = new Vector();
        for (nuocUong n : nuocUongDAO.getdsNuocUong()) {
            cbbTenNuoc.addItem(n.getTenNuoc());
        }
        for (ban b : banDAO.getdsban()) {
            cbbBan.addItem(b.getTenBan());
        }
        String tinhTrang;
        modelTbBan.setRowCount(0);
        for (ban b : banDAO.getdsban()) {
            tinhTrang = "Trống";
            if (b.getTinhTrang() == 1) {
                tinhTrang = "Có người";
            }
            Vector r = new Vector();
            r.add(b.getMaBan());
            r.add(b.getTenBan());
            r.add(tinhTrang);
            modelTbBan.addRow(r);
            dsMonTungBan.add(new Vector());
        }
    }

