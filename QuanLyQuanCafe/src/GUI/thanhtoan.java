    void themNuoc(int row) {
        boolean checkMon = true;
        banDAO.updateTinhTrang(tbBan.getValueAt(row, 0).toString().trim(), true);
        String tenNuoc = cbbTenNuoc.getSelectedItem().toString();
        int soLuong = Integer.parseInt(spnSoLuong.getValue().toString());
        for (int i = 0; i < modelTbNuocUong.getRowCount(); i++) {
            if (modelTbNuocUong.getValueAt(i, 1).toString().equals(tenNuoc)) {
                int soluongbd = Integer.parseInt(modelTbNuocUong.getValueAt(i, 3).toString());
                soluongbd += soLuong;
                modelTbNuocUong.setValueAt(soluongbd, i, 3);
                modelTbNuocUong.setValueAt(soluongbd * Float.parseFloat(modelTbNuocUong.getValueAt(i, 2).toString()), i, 4);
                checkMon = false;
                break;
            }
        }
        if (checkMon) {
            Vector r = nuocUongDAO.timTenNuocHD(tenNuoc, soLuong);
            modelTbNuocUong.addRow(r);
        }
        ((Vector) dsMonTungBan.get(row)).clear();
        for (int i = 0; i < modelTbNuocUong.getRowCount(); i++) {
            Vector r = new Vector();
            r.add(modelTbNuocUong.getValueAt(i, 0));
            r.add(modelTbNuocUong.getValueAt(i, 1));
            r.add(modelTbNuocUong.getValueAt(i, 2));
            r.add(modelTbNuocUong.getValueAt(i, 3));
            r.add(modelTbNuocUong.getValueAt(i, 4));
            ((Vector) dsMonTungBan.get(row)).add(r);
        }
        modelTbBan.setValueAt("Có người", row, 2);
    }

    void tinhTien() {
        float sum = 0;
        for (int i = 0; i < modelTbNuocUong.getRowCount(); i++) {
            sum += Float.parseFloat(modelTbNuocUong.getValueAt(i, 4).toString());
        }
        txtTongCong.setText("" + sum);
        float tienGiam = 0;

        if (!txtGiamGia.getText().equals("")) {
            txtTongCong1.setText(txtTongCong.getText());
            float giamGia = (Float.parseFloat(txtGiamGia.getText()));
            tienGiam = sum * giamGia / (float) 100;
        }
        txtThanhTien.setText("" + (sum - tienGiam));
    }
